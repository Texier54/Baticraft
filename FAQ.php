<div class="titre2">Les erreurs de connexion</div>
<br>	

Des soucis de connexion ? Voici la liste des erreurs que vous pouvez avoir.<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “Connection Lost, Failed to login : User not premium” (vous devez avoir acheté le jeu sur minecraft.net)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “failed to login : bad login” (erreur de pseudo ou mot de passe, souvent dû à un bug avec minecraft.net)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “Failed to connect : connection timed out” (le serveur met trop de temps à répondre, il est possible qu’il ai crash)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “error 504” (problème chez minecraft.net, réessayez plus tard)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “Internal exception: java.net.SocketException: Connection reset” (connexion impossible, serveur fermé ou indisponible)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “Unknown host “…….” (serveur introuvable, vérifiez que vous avez bien entré l’adresse « Baticraft.tk »)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “Connection refused: connect” (serveur coupé par l’administration pour une maintenance, il faut patienter)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “outdated Client” (version de minecraft non à jour)<br><br>

&nbsp;&nbsp;&nbsp;&nbsp; - “End of Stream” (tentative de reconnexion trop rapide, réessayez plus tard)<br><br><br>


