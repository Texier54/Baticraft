&nbsp;&nbsp;&nbsp;&nbsp; - le lag, dû le plus souvent à votre débit de connexion internet.<br><br>		

&nbsp;&nbsp;&nbsp;&nbsp; - le cuboïdage ce qui veut simplement dire que la zone à été
 protégée afin d’empêcher toute personne non autorisée d’accéder à cette zone.<br><br>	

&nbsp;&nbsp;&nbsp;&nbsp; - la limite du monde est atteinte, vous ne pouvez pas aller plus loin.<br><br><br>	


