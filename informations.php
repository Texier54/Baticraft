<br><br><br>





<div class="titre2">Ressources</div>
<br>
Une Map Ressource est à votre disposition, elle vous permet de récolter n’importe qu’elle ressource et gagner de l’argent grâce à votre métier. <br><br>
Vous pouvez vous y rendre par le biais des montgolfières disponible dans chaque ville.<br><br>
Cette Map est reset à chaque mise à jour ou plus souvent si elles tardent à sortir.
<br><br><br>


<div class="titre2">Métier</div>
<br>
Vous ne pouvez pratiquer qu'un seul métier à la fois.<br><br>
Pour changer de métier il suffit de taper <b>/jobschange [métier]</b> , 100po et vos levels de métier seront alors retirés.<br><br>
Les levels de métier montre tout simplement votre expérience et rien d'autre a part d'être choisis lorsque que l'on cherche une personne avec ce métier.
