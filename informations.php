<div class="titre2">Devenir citoyen</div>

<br>

Pour devenir Citoyen, il faut remplir le QCM des Visiteurs, accessible directement depuis le serveur. 
Toutes les réponses sont trouvables dans les panneaux d'informations du serveur et sur le site web
 <a class="d2" href="http://baticraft.tk/grade.php">sur cette page</a>. Pour commencer le QCM, tapez la commande « /qcm start » sur le serveur.<br><br><br>






<div class="titre2">Chat/Teamspeak</div>
<br>
Il est interdit d’avoir des propos racistes, xénophobes, offensants envers un groupe ou une communauté et de parler de piratage/hack/ect…<br><br>
Les propos diffamatoires seront punis (Notamment dans le cas de suspicion de « grief »)<br><br>
Évitez de flooder le chat, ceci est très gênant pour la communauté.
Afin de mieux communiquer entre tous les membres, n’écrivez surtout pas en langage SMS.<br><br>
Sur Teamspeak nous vous demandons d’avoir le même pseudo qu’InGame afin que tout le monde se repère plus facilement.<br><br><br>





<div class="titre2">Habitations</div>
<br>
Pour acheter une maison ou une parcelle vous devez en trouver une vous-même avec une pancarte de vente.
Vous n'avez le droit qu'à une maison par ville pas plus, tout comme les champs pour les fermiers mais vous pourrez en changer pour en prendre des plus grandes.
<br><br>
Vous pouvez aussi louer une habitation pour le temps que vous désirez.
Attention, tous les biens seront perdues si vous ne les récupérés pas avant la fin de votre location.
<br><br>
Pour déménager, vous devez acheter/louer votre futur maison (sauf si elle se situe dans la même ville ; 
dans ce cas utiliser les Ender Chest ou Shulker Box), déplacer vos affaires et revendez votre ancienne maison.<br><br>
Il est interdit de faire des cultures dans sa maison.
<br><br><br>





